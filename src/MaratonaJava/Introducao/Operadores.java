package MaratonaJava.Introducao;

public class Operadores {
    public static void main(String[] args) {
        // + - * /
        int num1 = 10;
        int num2 = 20;
        System.out.println(num2 - num1);
        System.out.println(num2 + num1);
        System.out.println(num2 / num1);
        System.out.println(num2 * num1);

        // % (resto), < , > , >= , <= , !=

        boolean isDezMaiorQueVinte = num1 > num2;
        System.out.println(isDezMaiorQueVinte);

        // && (AND) , || (OR) , ! (NOT)

        // += , -+ , ++ , --
    }
}
